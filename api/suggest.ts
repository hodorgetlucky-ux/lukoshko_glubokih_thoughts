export default async function handler(req: Request): Promise<Response> {
  if (req.method !== 'POST') {
    return new Response(JSON.stringify({ error: 'Method not allowed' }), {
      status: 405,
      headers: { 'Content-Type': 'application/json' },
    })
  }

  let body: { situation?: string }
  try {
    body = await req.json()
  } catch {
    return new Response(JSON.stringify({ error: 'Invalid JSON' }), {
      status: 400,
      headers: { 'Content-Type': 'application/json' },
    })
  }

  const situation = body.situation?.trim()
  if (!situation || situation.length < 5) {
    return new Response(JSON.stringify({ error: 'Ситуация слишком короткая' }), {
      status: 400,
      headers: { 'Content-Type': 'application/json' },
    })
  }

  if (situation.length > 2000) {
    return new Response(JSON.stringify({ error: 'Текст слишком длинный (максимум 2000 символов)' }), {
      status: 400,
      headers: { 'Content-Type': 'application/json' },
    })
  }

  try {
    const response = await client.messages.create({
      model: 'claude-opus-4-6',
      max_tokens: 1024,
      system: SYSTEM_PROMPT,
      messages: [
        { role: 'user', content: `Ситуация: ${situation}` },
      ],
    })

    const text = response.content.find(b => b.type === 'text')?.text ?? ''

    let parsed
    try {
      const cleaned = text.replace(/^```json\s*/i, '').replace(/\s*```$/, '').trim()
      parsed = JSON.parse(cleaned)
    } catch {
      return new Response(JSON.stringify({ error: 'Не удалось разобрать ответ ИИ. Попробуй ещё раз.' }), {
        status: 500,
        headers: { 'Content-Type': 'application/json' },
      })
    }

    return new Response(JSON.stringify(parsed), {
      status: 200,
      headers: { 'Content-Type': 'application/json' },
    })
  } catch (err) {
    console.error('Claude API error:', err)
    return new Response(JSON.stringify({ error: 'Ошибка сервиса. Попробуй через секунду.' }), {
      status: 500,
      headers: { 'Content-Type': 'application/json' },
    })
  }
}
